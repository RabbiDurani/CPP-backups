#include <iostream>
using namespace std;

int main()
{
    int a;
    float b;
    double c;
    char d;
    cout << "Size of (int) in bytes :" << sizeof(a) << endl;
    cout << "Size of (float) in bytes :" << sizeof(b) << endl;
    cout << "Size of (double) in bytes :" << sizeof(c) << endl;
    cout << "Size of (char) in bytes :" << sizeof(d) << endl;
    return 0;
}